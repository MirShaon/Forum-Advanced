    public function auth_callback($provider)
    {
        SocialAuth::login($provider, function($user, $details){

            $user->avatar = $details->avatar;
            $user->email = $details->email;
            $user->name = $details->nickname;
            
            $user->save();

        });

        return redirect('/forum');

        
